export function oscName(displayNo) {
  return OSC_NAMES[displayNo] ?? null
}

export function oscCategory(displayNo) {
  const r = OSC_CATEGORIES.find((r) => displayNo >= r.from && displayNo <= r.to)
  return r?.cat ?? null
}
